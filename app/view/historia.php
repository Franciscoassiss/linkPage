    <h2>O Início</h2>
    <p>
        A história inicia-se na Floresta Kokiri (Kokiri Forest), localizada no reino de Hyrule. Ela é habitada pelos 
        Kokiri, espíritos guardiões da floresta, seres similares a elfos, que moram em árvores, nunca se tornam adultos 
        e não sobrevivem fora da majestosa Floresta Kokiri. Além disso, quando atingem uma determinada idade recebem da 
        Grande Árvore Deku (Deku Tree) uma fada-guia. Dentre os Kokiri, Link era o único que ainda não possuía uma fada.S

        Certo dia, Link é chamado para ver a Grande Árvore através da fada Navi. Link segue até a árvore, que revela 
        que fora amaldiçoada por um homem vestindo uma armadura negra, e pede a ajuda de Link. Ele entra na árvore, 
        derrota um monstro chamado Rainha Gohma (uma espécie de aracnídeo), mas mesmo assim não consegue salvar a 
        árvore pois ela já estava velha e sabia que iria morrer logo.

        Logo depois de derrotar Gohma e sair da Grande Árvore Deku, esta lhe conta a história da Triforce e de como o 
        mundo foi construído através das três deusas, Farore, Din e Nayru. Quando já está quase morrendo, a árvore dá 
        a Link a Esmeralda Kokiri, a Pedra Espiritual da Floresta, uma das únicas coisas que ainda mantinha viva após 
        a maldição de Ganon, e pede para Link que visite a princesa Zelda no Castelo do Reino de Hyrule.

        Quando Link está quase saindo da floresta kokiri, Saria está a sua espera, dizendo que sabia que Link teria 
        que sair da floresta, e então dá a Link uma ocarina como prova de que a amizade deles nunca iria acabar.

        Link e Navi seguem para o castelo, e após despistarem os guardas, se encontram com Zelda, que diz que já o 
        aguardava, devido a uma premonição tida em sonho. Ela acredita — corretamente que — Ganondorf, rei dos Gerudos, 
        está a procura da lendária Triforce, um item poderoso que poderia dar a seu possuidor a capacidade de dominar 
        o mundo, quer fosse para o bem ou para o mal. Para entrar no Reino Sagrado, onde a Triforça se localiza, são 
        necessárias as três Pedras Espirituais, e vendo que Link já conseguira uma, Zelda pede para ele recuperar as 
        outras duas. Quando Link está voltando, se depara com Impa, a criada de Zelda que o ensina a canção de Zelda 
        (Zelda Lullaby).
    </p>
